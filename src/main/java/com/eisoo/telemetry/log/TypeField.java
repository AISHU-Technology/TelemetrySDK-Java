package com.eisoo.telemetry.log;


public class TypeField {
    //类型
    private String type;
    //内容
    private Object field;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Object getField() {
        return field;
    }

    public void setField(Object field) {
        this.field = field;
    }
}
